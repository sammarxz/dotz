import { cn } from "@/lib/utils";
import { Switch as SwitchBase } from "@base-ui/react/switch";

interface SwitchProps {
  checked: boolean;
  onCheckedChange: (checked: boolean) => void;
  disabled?: boolean;
}

export function SwitchComponent({
  checked,
  onCheckedChange,
  disabled,
}: SwitchProps) {
  return (
    <SwitchBase.Root
      checked={checked}
      onCheckedChange={onCheckedChange}
      disabled={disabled}
      className={cn(
        "relative inline-flex h-5 w-9 shrink-0 cursor-pointer rounded-full border border-zinc-800 transition-colors",
        "focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-zinc-500",
        "disabled:cursor-not-allowed disabled:opacity-50",
        checked ? "bg-white" : "bg-zinc-900"
      )}
    >
      <SwitchBase.Thumb
        className={cn(
          "pointer-events-none block h-4 w-4 rounded-full shadow-lg ring-0 transition-transform",
          checked
            ? "translate-x-4 bg-background"
            : "translate-x-0.5 bg-zinc-500"
        )}
      />
    </SwitchBase.Root>
  );
}
